const fs = require('fs');
const path = require('path');

const W1252_MAP = {
  0x80: [0xE2,0x82,0xAC], 0x82: [0xE2,0x80,0x9A], 0x83: [0xC6,0x92],
  0x84: [0xE2,0x80,0x9E], 0x85: [0xE2,0x80,0xA6], 0x86: [0xE2,0x80,0xA0],
  0x87: [0xE2,0x80,0xA1], 0x88: [0xCB,0x86],       0x89: [0xE2,0x80,0xB0],
  0x8A: [0xC5,0xA0],      0x8B: [0xE2,0x80,0xB9],  0x8C: [0xC5,0x92],
  0x8E: [0xC5,0xBD],      0x91: [0xE2,0x80,0x98],  0x92: [0xE2,0x80,0x99],
  0x93: [0xE2,0x80,0x9C], 0x94: [0xE2,0x80,0x9D],  0x95: [0xE2,0x80,0xA2],
  0x96: [0xE2,0x80,0x93], 0x97: [0xE2,0x80,0x94],  0x98: [0xCB,0x9C],
  0x99: [0xE2,0x84,0xA2], 0x9A: [0xC5,0xA1],       0x9B: [0xE2,0x80,0xBA],
  0x9C: [0xC5,0x93],      0x9E: [0xC5,0xBE],       0x9F: [0xC5,0xB8],
};

function fixFile(filePath) {
  const buf = fs.readFileSync(filePath);
  const result = [];
  let changed = false;
  let i = 0;
  while (i < buf.length) {
    const b = buf[i];
    if (b >= 0x80 && b <= 0x9F && W1252_MAP[b]) {
      result.push(...W1252_MAP[b]);
      changed = true;
    } else {
      result.push(b);
    }
    i++;
  }
  if (changed) {
    fs.writeFileSync(filePath, Buffer.from(result));
    console.log('Fixed:', filePath);
  } else {
    console.log('OK   :', filePath);
  }
}

const dir = 'src/components';
fs.readdirSync(dir)
  .filter(f => f.endsWith('.jsx') || f.endsWith('.js'))
  .forEach(f => fixFile(path.join(dir, f)));

// Also fix hooks and utils
['src/hooks', 'src/utils'].forEach(d => {
  if (fs.existsSync(d)) {
    fs.readdirSync(d).filter(f => f.endsWith('.js')).forEach(f => fixFile(path.join(d, f)));
  }
});
